package chamankumar.lld.oop.day02.accessmodifiers;

/**
 * The Main class serves as the entry point for the application.
 * It demonstrates a simple "Hello, World!" program to showcase
 * the basic structure of a Java application.
 *
 * Motivation:
 * This class is designed to provide a basic example of a Java program
 * with a main method, which is the starting point of execution.
 */
public class Main {
    public static void main(String[] args) {
        // Define a message to be printed
        String message = "Hello, World!";

        // Print the message to the console
        System.out.println(message);
    }
}